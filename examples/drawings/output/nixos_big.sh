#!/bin/sh

printf '\33[?25l'

move_up="\33[16F"

cleanup() {
    printf "$move_up\33[0J\33[0m\33[?25h"
    exit 0
}

trap cleanup INT

draw() {
    printf "$move_up$1"
    sleep 0.2
}

yes '' | head -n 16

while true; do
    draw '        \33[94m__    \33[96m____    __\n       \33[94m/  \134   \33[96m\134   \134  /  \134\n       \33[94m\134   \134   \33[96m\134   \134/   /\n     \33[94m___\134   \134___\33[96m\134      /\n    \33[94m/            \33[96m\134    /   \33[94m/\134\n   /______________\33[96m\134   \134  \33[94m/  \134\n        \33[96m/   /      \134   \134\33[94m/   /\n \33[96m______/   /        \134  \33[94m/   /___\n\33[96m/         /          \134\33[94m/        \134\n\33[96m\134____    /\33[94m\134          /   ______/\n    \33[96m/   /  \33[94m\134        /   /\n   \33[96m/   /\33[94m\134   \134\33[96m______\33[94m/\33[96m___\33[94m/\33[96m_____\n   \134  /  \33[94m\134   \134              \33[96m/\n    \134/   \33[94m/    \134\33[96m____    ____/\n        \33[94m/      \134   \33[96m\134   \134\n       \33[94m/   /\134   \134   \33[96m\134   \134\n       \33[94m\134__/  \134___\134   \33[96m\134__/'
done
cleanup

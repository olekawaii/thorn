#!/bin/sh

printf '\33[?25l'

move_up="\33[12F"

cleanup() {
    printf "$move_up\33[0J\33[0m\33[?25h"
    exit 0
}

trap cleanup INT

draw() {
    printf "$move_up$1"
    sleep 0.2
}

yes '' | head -n 12

while true; do
    draw '      \33[94m_    \33[96m___   _\n     \33[94m/ \134   \33[96m\134  \134 / \134\n     \33[94m\134  \134   \33[96m\134  \47  /\n   \33[94m---`  `---\33[96m\134   /  \33[94m,\n  /___________\33[96m\134  \134 \33[94m/ \134\n     \33[96m/  /      \134  \33[94m/  /\n\33[96m/\47\47\47\47  /        \134\33[94m/  \47\47\47\134\n\33[96m\134--.  /\134        \33[94m/  ----/\n  \33[96m/  /  \33[94m\134      /  /\n  \33[96m\134 / \33[94m\134  \134\33[96m------------\n   `  \33[94m/   \134 \33[96m___   __/\n     \33[94m/  ,  \134   \33[96m\134  \134\n     \33[94m\134_/ \134__\134   \33[96m\134_/'
done
cleanup
